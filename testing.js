// How to get element by ID: getElementById();
// Gets the element "demo" then changes its innerHTML to "Hello Javascript"
document.getElementById("demo").innerHTML = "Hello JavaScript"; 
// Gets the "demo" element and changes its fontSize to 35px
document.getElementById("demo").style.fontSize = "35px"; 
// Gets the "Demo" element and Hides elements  using "None"
document.getElementById("demo").style.display = "none"; 
// Gets the "demo element and now shows blocked HTML elements using "Block"
document.getElementById("demo").style.display = "block"; 
// How Javascript code can be inserted inside HTML file
<script>
	document.getElementById("demo").innerHTML = "My First JavaScript";
</script> 




















































































































































































































																	









